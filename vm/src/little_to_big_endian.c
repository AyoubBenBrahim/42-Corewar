#include "../inc/cor.h"

u_int32_t	little_to_big_endian(u_int32_t x)
{
	return (((x >> 24) & 0x000000ff)
			| ((x >> 8) & 0x0000ff00)
			| ((x << 8) & 0x00ff0000)
			| ((x << 24) & 0xff000000));
}
